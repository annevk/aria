		<h1>ARIA Authoring Practices Task Force Work Statement</h1>
		<p>The ARIA Authoring Practices (APG) Task Force is a task force of the <a href="https://www.w3.org/WAI/ARIA/">Accessible Rich Internet Applications Working Group (ARIA Working Group)</a>. It assists this Working Group with the work identified below.</p>
		<section id="status">
			<h2>Status</h2>
			<p>The APG Task Force was <a href="https://lists.w3.org/Archives/Public/public-aria-admin/2017Sep/0025.html">approved on 14 September 2017</a> and is now active. The <a href=".">Task Force home page</a> contains information about the operation and resources of the group.</p>
		</section>
		<section id="objective">
			<h2>Objective</h2>
			<p>The mission of the APG Task Force is to develop <a href="http://w3c.github.io/aria-practices/">WAI-ARIA Authoring Practices</a>, in consultation with the ARIA Working Group. The task force provides a focused forum for this work while the Working Group continues its work on ARIA development.</p>
		</section>
		<section id="scope">
			<h2>Scope of Work</h2>
			<p>WAI-ARIA Authoring Practices provides web content developers with an understanding of how to use WAI-ARIA to create accessible rich internet applications. It describes considerations that might not be evident to most authors from the WAI-ARIA specification alone and recommends approaches to make widgets, navigation, and behaviors accessible using WAI-ARIA roles, states, and properties. Design patterns and working examples show what authors need to do in addition to using WAI-ARIA semantics to make content fully accessible, such as providing keyboard navigation support and using familiar widget designs.</p>
		</section>
		<section id="approach">
			<h2>Approach</h2>
			<p>The task force will work within the ARIA Working Group to develop and refine the APG. Development of the APG will be closely tied to the development of ARIA, but focused more on authoring perspectives. The task force will actively engage with developers in the field to ensure its guidance is understandable and useful to content developers. It will publish Working Drafts of WAI-ARIA Authoring Practices via the ARIA Working Group, and at key points will publish the APG as a W3C Working Group Note.</p>
		</section>
		<section id="communication">
			<h2>Communication</h2>
			<p>APG Task Force communications and discussions are visible to the public. The <a href=".">@@task force home page</a> details communication mechanisms in use. The task force will:</p>
			<ul>
				<li>Support synchronous (e.g., teleconferences, face-to-face meetings) and asynchronous (e.g., mailing list discussions, issue trackers, wiki, surveys, etc.) methods of communication;</li>
				<li>Use the procedures of the <a href="https://www.w3.org/WAI/ARIA/decision-policy">ARIA Working Group Decision Policy</a> to make decisions;</li>
				<li>Provide updates and feedback to ARIA Working Group teleconferences, as appropriate. These liaisons are primarily the responsibility of the APG Task Force Facilitators;</li>
				<li>Maintain information resulting from its activities (deliverables, participants, face-to-face meetings, teleconferences, etc.) on the <a href=".">APG Task Force home page</a>.</li>
			</ul>
		</section>
		<section id="participation">
			<h2>Participation</h2>
			<p>Any participant in the ARIA Working Group may participate in the APG Task Force. Participants should expect to dedicate 2 to 4 hours per week for APG Task Force work, which is considered part of the overall work commitment to the ARIA Working Group: </p>
			<ul>
				<li>Remain current on the APG Task Force mailing list and respond to postings in a timely manner; </li>
				<li>Provide feedback to editors of specifications via email or by filing bugs; </li>
				<li>Participate in weekly APG Task Force teleconferences, or send regrets to the APG Task Force mailing list. </li>
			</ul>
			<p>Participants may also join APG Task Force sub-groups. Sub-groups take on specific assignments for the APG Task Force. </p>
			<p>If you are interested in becoming a participant of the APG Task Force or have any questions regarding its work, contact the task force facilitators. </p>
