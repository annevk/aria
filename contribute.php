		<h1>ARIA Contribution</h1>

		<p>There are a number of ways non-participants in the ARIA WG can contribute to the work. If you wish to be an active contributer, however, you should <a href="participation">join the Working Group</a>.</p>

		<section id="comments">
			<h2>Public comments</h2>
			<p>The ARIA WG accepts public comments on publications on the <a href="http://lists.w3.org/Archives/Public/public-aria/">public-aria</a> list. This is primarily used for input on formal publications. </p>
		</section>

		<section id="github">
			<h2>Github issues and pull requests</h2>
			<p>Technical work is done in a set of GitHub repositories. Work on a given feature may take place in several repositories, and follows the <a href="workflow">ARIA Working Group Work Flow</a>. Repositories maintained by the ARIA Working Group include:</p>
			<ul>
				<li><a href="https://github.com/w3c/aria/">w3c/aria</a> for Accessible Rich Internet Applications;</li>
				<li><a href="https://github.com/w3c/core-aam/">w3c/core-aam</a> for Core Accessibility API Mappings;</li>
				<li><a href="https://github.com/w3c/accname/">w3c/accname</a> for Accessible Name and Description Computation;</li>
				<li><a href="https://github.com/w3c/dpub-aria/">w3c/dpub-aria</a> for Digital Publishing WAI-ARIA Module, now developed by the <a href="https://www.w3.org/publishing/groups/publ-wg/">Publishing Working Group</a>;</li>
				<li><a href="https://github.com/w3c/dpub-aam/">w3c/dpub-aam</a> for Digital Publishing Accessibility API Mappings;</li>
				<li><a href="https://github.com/w3c/graphics-aria/">w3c/graphics-aria</a> for WAI-ARIA Graphics Module;</li>
				<li><a href="https://github.com/w3c/graphics-aam/">w3c/graphics-aam</a> for Graphics Accessibility API Mappings;</li>
				<li><a href="https://github.com/w3c/svg-aam/">w3c/svg-aam</a> for SVG Accessibility API Mappings, now developed by the <a href="http://www.w3.org/Graphics/SVG/WG/">SVG Working Group</a>;</li>
				<li><a href="https://github.com/w3c/html-aam/">w3c/html-aam</a> for HTML Accessibility API Mappings, now developed by the <a href="http://www.w3.org/WebPlatform/WG/">Web Platform Working Group</a>;</li>
				<li><a href="https://github.com/w3c/css-aam/">w3c/css-aam</a> for CSS Accessibility API Mappings, with the <a href="https://www.w3.org/WAI/APA/task-forces/css-a11y/">CSS Accessibility Task Force</a>;</li>
				<li><a href="https://github.com/w3c/aria-practices/">w3c/aria-practices</a> for WAI-ARIA Authoring Practices, with the <a href="https://www.w3.org/WAI/ARIA/task-forces/practices/">ARIA Authoring Practices Task Force</a>;</li>
                <li><a href="https://github.com/w3c/aria-at/">w3c/aria-at</a> for Assistive Technology ARIA Experience Assessment, with the <a href="https://www.w3.org/community/aria-at/">ARIA and Assistive Technologies Community Group</a>;</li>
			</ul>
			<p>A <a href="http://github.com/">GitHub account</a> is required for the following functionality. In addition to using the comments list described above to submit input, you may:</p>
			<ul>
				<li><a href="https://help.github.com/articles/watching-repositories/">Watch the repository</a>;</li>
				<li><a href="https://guides.github.com/features/issues/">File issues</a>;</li>
				<li>Submit <a href="https://help.github.com/articles/using-pull-requests/">pull requests</a>.</li>
			</ul>
		</section>

